create view vw_account_fullname as
select id,
       (select GROUP_CONCAT(account_name SEPARATOR ' - ')
        from account b
        where id = a.id
           or id in (
            select SUBSTRING_INDEX(a.hierarchy_id, '_', 1)
            union all
            select SUBSTRING_INDEX(SUBSTRING_INDEX(a.hierarchy_id, '_', 2),
                                   '_', -1)
            union all
            select SUBSTRING_INDEX(SUBSTRING_INDEX(a.hierarchy_id, '_', 3),
                                   '_', -1)
            union all
            select SUBSTRING_INDEX(SUBSTRING_INDEX(a.hierarchy_id, '_', 4),
                                   '_', -1)
            union all
            select SUBSTRING_INDEX(SUBSTRING_INDEX(a.hierarchy_id, '_', 5),
                                   '_', -1)
            union all
            select SUBSTRING_INDEX(SUBSTRING_INDEX(a.hierarchy_id, '_', 6),
                                   '_', -1)
        )
        group by null
        order by length(hierarchy_id)
)as full_account_name from account a
;


